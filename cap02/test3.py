# Operadores lógicos booleanos

log_t = True
print(f"A variável log_t contém o valor {log_t}")

log_t = False
print(f"A variável log_t agora contém o valor {log_t}")

log_t = 5 > 2
print(f"5 é maior que 2? {log_t}")

log_t = 5 < 2
print(f"5 é menor que 2? {log_t}")

log_t = 5 >= 2
print(f"5 é maior ou igual a 2? {log_t}")

log_t = 5 <= 2
print(f"5 é menor ou igual a 2? {log_t}")

log_t = 5 == 2
print(f"5 é igual a 2? {log_t}")

log_t = 5 != 2 
print(f"5 é diferente de 2? {log_t}")

log_t = bool(0)
print(f"O valor booleano de 0 é {log_t}")

log_t = bool(15)
print(f"O valor booleano de 15 é {log_t}")


